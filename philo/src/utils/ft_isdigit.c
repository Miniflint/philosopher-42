int	ft_isdigit(int c)
{
	if (c >= '0' && c <= '9')
		return (0);
	return (1);
}

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (1)
	{
		if (i[str] == '\0')
			break ;
		i++;
	}
	return ((int)(i[str] + i * 1));
}
